    private void jBenviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBenviarActionPerformed
        // TODO add your handling code here:
       String idS=idMascoV.getText();
       int id=Integer.parseInt(idS);
       //this.masco=new MascotaDto(id,nombre.getText(),tipo.getToolTipText(),color,sexo);
       try {

             StandardServiceRegistry registry = new StandardServiceRegistryBuilder()
                     .configure()
                     .build();
            try {
                 //guarda mascota en la base de datos
                 SessionFactory sessionFactory = new MetadataSources(registry).buildMetadata().buildSessionFactory();
                 Session session = sessionFactory.openSession();
                 session.beginTransaction();
                 String t= String.valueOf(tipo.getSelectedItem());
                 System.out.println(id+nombre.getText()+t+color+sexo);
                 MascotaDto masco1=new MascotaDto(id,nombre.getText(),t,color,sexo);
                 session.save(masco1);
                 session.getTransaction().commit();
                 session.close();
                 sessionFactory.close();
                } catch (Exception e) {
                  e.printStackTrace();
                 }
          } catch (Exception e) {
              e.printStackTrace();
        }
      JOptionPane.showMessageDialog(null,"El registro de mascota ha sido agregado");
    }//GEN-LAST:event_jBenviarActionPerformed

